import { access, cp, mkdir, readdir, readFile, rm, writeFile } from "node:fs/promises";
import path from "node:path";

const SOURCE_DIR = path.join(process.cwd(), "data", "precompute");
const TARGET_DIR = path.join(process.cwd(), "public", "precompute");
const GENERATED_TS = path.join(process.cwd(), "lib", "snapshot-data.generated.ts");

async function sourceExists() {
  try {
    await access(SOURCE_DIR);
    return true;
  } catch {
    return false;
  }
}

async function generateSnapshotModule() {
  const versions = await readdir(SOURCE_DIR).catch(() => []);
  const versionMap = {};

  for (const version of versions) {
    const versionDir = path.join(SOURCE_DIR, version);
    const summaryPath = path.join(versionDir, "_run-summary.json");
    let summaryRaw;
    try {
      summaryRaw = await readFile(summaryPath, "utf8");
    } catch {
      continue;
    }

    const csvMap = {};
    const files = await readdir(versionDir).catch(() => []);
    for (const file of files) {
      if (!file.endsWith(".csv")) continue;
      const slug = file.replace(/\.csv$/, "");
      const csvContent = await readFile(path.join(versionDir, file), "utf8");
      csvMap[slug] = csvContent;
    }

    versionMap[version] = { summary: summaryRaw, csvs: csvMap };
  }

  const tsContent = `// AUTO-GENERATED by scripts/sync-precompute-to-public.mjs — do not edit
/* eslint-disable */

type SnapshotVersionData = {
  summary: string;
  csvs: Record<string, string>;
};

const SNAPSHOT_DATA: Record<string, SnapshotVersionData> = ${JSON.stringify(versionMap, null, 2)};

function isTestEnv() {
  return typeof process !== "undefined" && process.env?.NODE_ENV === "test";
}

export function getSnapshotSummary(version: string): string | null {
  if (isTestEnv()) return null;
  return SNAPSHOT_DATA[version]?.summary ?? null;
}

export function getSnapshotCsv(version: string, slug: string): string | null {
  if (isTestEnv()) return null;
  return SNAPSHOT_DATA[version]?.csvs[slug] ?? null;
}

export function getAvailableVersions(): string[] {
  return Object.keys(SNAPSHOT_DATA);
}
`;

  await mkdir(path.dirname(GENERATED_TS), { recursive: true });
  await writeFile(GENERATED_TS, tsContent, "utf8");
  console.log(`[snapshot-sync] generated ${GENERATED_TS} (${versions.length} version(s))`);
}

async function main() {
  if (!(await sourceExists())) {
    console.log(`[snapshot-sync] skipped: missing source dir ${SOURCE_DIR}`);
    return;
  }

  await mkdir(path.dirname(TARGET_DIR), { recursive: true });
  await rm(TARGET_DIR, { recursive: true, force: true });
  await cp(SOURCE_DIR, TARGET_DIR, { recursive: true, force: true });
  console.log(`[snapshot-sync] copied ${SOURCE_DIR} -> ${TARGET_DIR}`);

  await generateSnapshotModule();
}

main().catch((error) => {
  console.error("[snapshot-sync] fatal error", error);
  process.exitCode = 1;
});
